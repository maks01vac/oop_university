# экран должны быть включены обычная цена за буханку, цена со скидкой 
# и общая стоимость приобретенного хлеба. Все значения должны быть выведены
#  на отдельных строках с соответствующими описаниями. Используйте
# для вывода формат с двумя знаками после запятой и выровненным 
# разделителем.

yesterdaysBread = int(input('Введите количество вчерашних буханок:'))

price = 3.49
discount = price*0.6
totalPrice = yesterdaysBread*discount

print('Стоимость буханки хлеба со скидкой:$',price)
print('Стоимость буханки хлеба без скидки:$',discount)
print('Итоговая стоимость за все вчерашние буханки хлеба:$',totalPrice)






