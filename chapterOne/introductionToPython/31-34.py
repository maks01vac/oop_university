#33 Напишите программу, запрашивающую у пользователя три целых числа 
# и выводящую их в упорядоченном виде – по возрастанию. Используйте 
# функции min и max для нахождения наименьшего и наибольшего значений. 
# Оставшееся число можно найти путем вычитания из суммы трех введенных 
# чисел максимального и минимального.

array = input('Введите 3 целых числа через пробел:').split()

if len(array) == 3:

    for i in range(len(array)):
        array[i] = int(array[i])

    array.sort()
    print(array)

else:

    print('Вы ввели не 3 целых числа')


